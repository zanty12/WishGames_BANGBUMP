template<class T>
class SWITCH_CHECK {
private:
	T previous;
	T now;

public:
	SWITCH_CHECK(const T &value) : now(value), previous(value) { }
	T &operator =(T value) {
		previous = now;
		now = value;
	}
	bool IsSwitch(void) { return previous != now; }
	T Get(void) { return now; }
	T GetPrevious(void) { return previous; }
};

