namespace WIN {
	class Time {
	private:
		DWORD startTime = 0ul;		// 計測開始時間
		DWORD nowTime = 0ul;		// 現在の計測時間
		bool isActive = false;		// 計測しているか

	public:

		/// <summary>
		/// 計測開始
		/// </summary>
		void Start(void);

		/// <summary>
		/// 計測終了
		/// </summary>
		void Stop(void);

		/// <summary>
		/// 再測定開始
		/// </summary>
		void Restart(void);

		/// <summary>
		/// 現在の時間
		/// </summary>
		/// <returns></returns>
		unsigned int GetNowTime(void);
	};
}

